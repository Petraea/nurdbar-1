asciiText = function(text) {
  var text = text.toUpperCase();
  var cols = text.split('').length;
  var rows = 10;
  var output = '';
  for (row = 0; row < rows; row++) {
    for (col = 0; col < cols; col++) {
      if (!font[text[col]]) {
        output += '     ';
      } else {
        output += fontSprite[row].slice(font[text[col]][0], font[text[col]][1]);
      }
    }
    output += '\n';
  }
  return output;
}
